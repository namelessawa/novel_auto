"""Rubric scoring judge prompt — v1.

3 维度, 1-5 分制, 每档锚点描述写在 prompt 里 (反 judge 中位回归).
"""

from __future__ import annotations

RUBRIC_VERSION = "rubric_v1"


RUBRIC_PROMPT_V1 = """\
你是一位严格的中文连载小说编辑。给下面这段正文按 3 个维度打分,
每个维度独立给 1-5 分.

# 维度与锚点

## 1. coherence (连贯性)
- 1: 句子之间彼此孤立, 因果突兀, 多处明显矛盾
- 2: 大段读得通, 个别地方需要回看才接得上
- 3: 整体顺畅, 偶有小衔接生硬
- 4: 句句紧扣, 段内逻辑清晰, 段末与开头自然呼应
- 5: 因果链层次分明, 读者一遍读完不需要回头

## 2. character_voice (角色声音一致性)
- 1: 角色说话像换了人, 或台词全无个性差异
- 2: 主角声音勉强稳定, 配角声音随机
- 3: 主角声音稳定, 配角靠场景能区分
- 4: 多角色台词能盲测分辨, 动作姿态贴合各自性格
- 5: 声纹与人物刻画完全统一, 内心独白与台词互相印证

## 3. plot_progression (情节推进)
- 1: 段末读者未获得任何新事实, 全是氛围
- 2: 模糊暗示有事在发生, 但落到具体细节都没说清
- 3: 段末读者至少明确一个新事实 (人物/物件/时间/动机)
- 4: 段末既有新事实, 又埋下一个等待回应的悬念
- 5: 段末同时推进主线 + 触及多个待回收线索, 节奏感强

# 待评段落

{text}

# 输出格式 (严格 JSON, 不要 markdown 围栏, 不要解释)

{{
  "coherence": <1-5 整数>,
  "character_voice": <1-5 整数>,
  "plot_progression": <1-5 整数>,
  "reason": "一句话中文说明给分最关键的观察(≤40 字)"
}}
"""


